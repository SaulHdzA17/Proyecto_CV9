      public void Eliminar(){
        
        int opt=JOptionPane.showConfirmDialog(null, "¿Desea eliminar el registro seleccionado?", "Eliminación", JOptionPane.YES_NO_OPTION);
        
        if(opt==0){
        int fila=TablaAgendarVisitas.getSelectedRow();
        String valor =TablaAgendarVisitas.getValueAt(fila,0).toString();
            try {
                PreparedStatement delete = connection.prepareStatement("Delete from VisitasAgendadas where id='"+valor+"'");
                delete.executeUpdate();
                JOptionPane.showMessageDialog(null, "Eliminación exitosa");
                Mostrar("VisitasAgendadas");
            } catch (Exception e) {
                System.out.println(e.toString());
            }
        }
    }
      
     public void Actualizar(){

        int fila = TablaAgendarVisitas.getSelectedRow();
    
        int id =Integer.parseInt(this.TablaAgendarVisitas.getValueAt(fila, 0).toString());
        String N =TablaAgendarVisitas.getValueAt(fila,1).toString();
        String C =TablaAgendarVisitas.getValueAt(fila,2).toString();
        String T =TablaAgendarVisitas.getValueAt(fila,3).toString();
        String F =TablaAgendarVisitas.getValueAt(fila,4).toString();
        String HI =TablaAgendarVisitas.getValueAt(fila,5).toString();
        String HF =TablaAgendarVisitas.getValueAt(fila,6).toString();
        String CM =TablaAgendarVisitas.getValueAt(fila,7).toString();
        String E =TablaAgendarVisitas.getValueAt(fila,8).toString();
    
        try {
            PreparedStatement actu= connection.prepareStatement("Update VisitasAgendadas set NombreVisitante='"+N+"', CorreoVisitante='"+C+"', TelefonoVisitante='"+T+"', FechaVisita='"+F+"', HoraInicio='"+HI+"', HoraFin='"+HF+"', Comentarios='"+CM+"', EstadoVisita='"+E+"' where id='"+id+"'");
            actu.executeUpdate();
            Mostrar("VisitasAgendadas");
            JOptionPane.showMessageDialog(null,"Actualizacion exitosa");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e +"No se actualizó el registro");
        }

}
     
